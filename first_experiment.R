# To demonstrate assignment operator and expressions.

a <- 10
b <- 5

sum <- a + b
difference <- a - b
product <- a * b
quotient <- a / b
powerab <- a ^ b

is_greater <- a > b
is_equal <- a == b

cat("The sum of x and y is: ", sum, "\n")
cat("The difference of x and y is: ", difference, "\n")
cat("The product of x and y is: ", product, "\n")
cat("The division of x and y is: ", quotient, "\n")
cat("The power of x and y is: ", powerab, "\n")
cat("a > b?: ", is_greater, "\n")
cat("a = b ?: ", is_equal, "\n")
